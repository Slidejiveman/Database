CUST_BALANCE FLOAT(8)
);

CREATE TABLE CUSTOMER_2 (
CUST_NUM INT PRIMARY KEY,
CUST_LNAME VARCHAR(20),
CUST_FNAME VARCHAR(20)
);

CREATE TABLE INVOICE (
INV_NUM INT PRIMARY KEY,
CUST_NUM INT, 
INV_DATE DATE,
INV_AMOUNT FLOAT(8)
);

